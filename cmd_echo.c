#include "./include/minishell.h"

void    cmd_echo(char **argv)
{
    int i;

    i = 0;
    while (argv[i++])
    {
        ft_putstr_fd(argv[i], 1);
        if (argv[i + 1])
            write(1, " ", 1);
    }
    write(1, "\n", 1);
}
